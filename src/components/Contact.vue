<script setup lang="ts">
import { ref, reactive } from 'vue';

// Estado do formulário
const form = reactive({
  name: '',
  email: '',
  service: '',
  message: ''
});

const isSubmitting = ref(false);
const formError = ref('');

// Função para enviar para o WhatsApp
function sendToWhatsApp() {
  try {
    isSubmitting.value = true;
    formError.value = '';
    
    // Validação básica
    if (!form.name || !form.email || !form.service || !form.message) {
      formError.value = 'Por favor, preencha todos os campos.';
      isSubmitting.value = false;
      return;
    }
    
    const phoneNumber = '5567992803728';
    
    const message = `*Contato via Site - Machado Fotografia e Eventos*
    
*Nome:* ${form.name}
*Email:* ${form.email}
*Serviço:* ${form.service}
*Mensagem:* ${form.message}`;
    
    // Codificar a mensagem para URL
    const encodedMessage = encodeURIComponent(message);
    
    // Criar o link do WhatsApp
    const whatsappLink = `https://wa.me/${phoneNumber}?text=${encodedMessage}`;
    
    // Redirecionar para o WhatsApp
    window.open(whatsappLink, '_blank');
    
    // Limpar formulário após envio
    setTimeout(() => {
      form.name = '';
      form.email = '';
      form.service = '';
      form.message = '';
      isSubmitting.value = false;
    }, 500);
    
  } catch (error) {
    console.error('Erro ao enviar mensagem:', error);
    formError.value = 'Ocorreu um erro ao tentar enviar sua mensagem. Por favor, tente novamente.';
    isSubmitting.value = false;
  }
}
</script>